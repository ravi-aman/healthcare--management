function addVariablesForColors({ addBase, theme }: { addBase: (base: any) => void; theme: (key: string) => any }) {
  const allColors = theme('colors');
  const newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ':root': newVars,
  });
}

